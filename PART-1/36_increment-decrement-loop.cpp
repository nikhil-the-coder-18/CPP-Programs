#include<iostream>
using namespace std;
int main ()
{
    // For Increment....
    for ( int i=1 ; i <= 10 ; i++)
    {
        cout << i << "  ";
    }

    cout << endl;

    //For Decrement....
    for (int j = 10 ; j >= 1 ; j--)
    {
        cout << j << "  ";
    }

    return 0;
}